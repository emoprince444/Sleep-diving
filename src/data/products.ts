function prices(rows: [string, number, number][]) {
  return rows.map(([size, wholesalePrice, rrp]) => {
    const oldPrice = Math.ceil((rrp * 1.16) / 100) * 100
    return {
      size,
      wholesalePrice,
      rrp,
      oldPrice,
      savings: oldPrice - rrp,
    }
  })
}

